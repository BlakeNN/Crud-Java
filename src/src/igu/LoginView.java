    private void loginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtnActionPerformed
        // Obteniendo Usuario y Contrase침a
        String userEmail = userInput.getText();
        String userPass = passInput.getText();

        if (userEmail.isEmpty() || userPass.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe rellenar todos los campos");
        } else {
            try (Connection conn = Conexion.getConexion()) { // Conectando a la DB
                if (conn != null) {
                    // Creando Query
                    String logInQuery = "SELECT * FROM usuarios WHERE email = ? AND clave = ?";
                    // Creando Statement de la Query
                    PreparedStatement pst = conn.prepareStatement(logInQuery);
                    // Preparando el Statement
                    pst.setString(1, userEmail);
                    pst.setString(2, userPass);
                    // Ejecutando la Consulta
                    ResultSet rs = pst.executeQuery();
                    if (rs.next()) { // Redirigiendo segun el Rol del Usuario
                        ContenedorDatos.getInstance().setIdUser(rs.getInt("id"));
                        if(rs.getString("rol").equals("cli")) { // Vista de Cliente
                            ClienteView clienteView = new ClienteView();
                            clienteView.setVisible(true);
                            clienteView.setLocationRelativeTo(null);
                        } if(rs.getString("rol").equals("insp")) { // Vista de Inspector
                            BuscarTurnoView buscarTurno = new BuscarTurnoView();
                            buscarTurno.setVisible(true);
                            buscarTurno.setLocationRelativeTo(null);
                        }
                        dispose(); // Cerrando Ventana Actual
                    } else {
                        JOptionPane.showMessageDialog(this, "Usuario o contrase침a incorrectos");
                    }
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_loginBtnActionPerformed

